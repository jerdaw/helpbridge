const locales = ["en", "fr", "ar", "zh-Hans", "es", "pa", "pt"]

for (const locale of locales) {
  const filePath = path.join(process.cwd(), `messages/${locale}.json`)
  if (fs.existsSync(filePath)) {
    const content = JSON.parse(fs.readFileSync(filePath, "utf8"))

    // Impact
    if (impactTranslations[locale as keyof typeof impactTranslations]) {
      content.Impact = impactTranslations[locale as keyof typeof impactTranslations]
    }

    // Trust
    if (trustTranslations[locale as keyof typeof trustTranslations]) {
      content.Trust = trustTranslations[locale as keyof typeof trustTranslations]
    }

    // Feedback Additions
    if (feedbackAdditions[locale as keyof typeof feedbackAdditions]) {
      Object.assign(content.Feedback, feedbackAdditions[locale as keyof typeof feedbackAdditions])
    }

    fs.writeFileSync(filePath, JSON.stringify(content, null, 2))
    console.log(`✅ Updated messages/${locale}.json`)
  }
}
